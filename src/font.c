const uint8_t *glyph_for(char c)
{
   if (c >= 'a' && c <= 'z')
      c -= 'a' - 'A';
   if (c >= 'A' && c <= 'Z')
      return font_upper[c - 'A'];
   if (c >= '0' && c <= '9')
      return font_digit[c - '0'];
   if (c == '-')
      return font_minus;
   if (c == ':')
      return font_colon;
   if (c == '.')
      return font_dot;
   if (c == '!')
      return font_bang;
   if (c == '/')
      return font_slash;
   if (c == '+')
      return font_plus;
   if (c == '(')
      return font_lpar;
   if (c == ')')
      return font_rpar;
   if (c == '%')
      return font_pct;
   if (c == '<')
      return font_larrow; /* backspace key label */
   if (c == '_')
      return font_uscore; /* keypad entry placeholder */
   if (c == '>')
      return font_rarrow; /* primary-sensor marker */
   if (c == ',')
      return font_comma;
   if (c == '?')
      return font_quest;
   return 0; /* space / unknown: blank cell */
}

int str_len(const char *s)
{
   int n = 0;
   while (s[n])
      n++;
   return n;
}
